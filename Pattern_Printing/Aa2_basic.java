package Pattern_Printing;

public class Aa2_basic {
    public static void main(String[] args) {
        
        int row = 5;
        int col = 3;
        for ( int i = 1; i <= row; i++){
            for(int j= 1; j <= col; j++){
            System.out.print("* ");
        }
        System.out.println();
    }
}

    
}
