package Arrays;

public class ForEachloop {
    public static void main(String[] args) {
        int [] arr = {5,7,3,9,2,6,4};
        for(int ele : arr){ // READED AS:- for (int element<on place on element you can write anything like, bhan,raj...> in array)
            // by use of for each loop, we can not change the element it is just use for display this elements
            System.out.print(ele+" ");
        }       
    }
    
}
